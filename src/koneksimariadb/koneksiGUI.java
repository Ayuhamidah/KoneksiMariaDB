    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            sql = "SELECT * FROM admin WHERE username='"+txt_name.getText()+"'AND password='"+jPasswordField1.getText()+"'";
            rs = stat.executeQuery(sql);
            if (rs.next()) {
                if (txt_name.getText().equals(rs.getString("username")) && jPasswordField1.getText().equals(rs.getString("password"))) {
                  JOptionPane.showMessageDialog(null, "Berhasil Login");
                }
            } else { JOptionPane.showMessageDialog(null, "username atau password salah");
            }
        }catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

